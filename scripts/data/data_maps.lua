----------------------------------------------------------------------------
-- 构建 MAPS 表 (结构化, 带计算战力)
----------------------------------------------------------------------------

---@class MapConfig
---@field id number
---@field name string
---@field theme string
---@field soldiers string[]
---@field normalGuards string[]
---@field eliteGuards string[]
---@field boss string
---@field normalDrops string[]
---@field bossDrops string[]
---@field recruit string
---@field power number

--- 100 图完整配置
---@type table<number, MapConfig>
M.MAPS = {}

for _, r in ipairs(RAW) do
    local id = r[1]
    M.MAPS[id] = {
        id           = id,
        name         = r[2],
        theme        = r[3],
        soldiers     = split(r[4]),
        normalGuards = split(r[5]),
        eliteGuards  = split(r[6]),
        boss         = r[7],
        normalDrops  = split(r[8]),
        bossDrops    = split(r[9]),
        recruit      = r[10],
        power        = calcPower(id),
    }
end

----------------------------------------------------------------------------
-- 便捷 API
----------------------------------------------------------------------------

--- 获取指定地图配置
---@param mapId number
---@return MapConfig|nil
function M.Get(mapId)
    return M.MAPS[mapId]
end

--- 获取地图推荐战力
---@param mapId number
---@return number
function M.GetPower(mapId)
    local m = M.MAPS[mapId]
    return m and m.power or 0
end

--- 获取指定节点的强度 (= 本图战力 × 节点强度曲线)
---@param mapId number
---@param nodeIdx number 1~24
---@return number
function M.GetNodePower(mapId, nodeIdx)
    local basePower = M.GetPower(mapId)
    local curve = M.STRENGTH_CURVE[nodeIdx] or 1.0
    return math.floor(basePower * curve)
end

--- 获取节点体力消耗
---@param nodeIdx number 1~24
---@return number
function M.GetNodeStamina(nodeIdx)
    local tpl = M.NODE_TEMPLATE[nodeIdx]
    if not tpl then return 0 end
    return M.NODE_STAMINA[tpl[2]] or 0
end

--- 获取节点类型
---@param nodeIdx number 1~24
---@return string
function M.GetNodeType(nodeIdx)
    local tpl = M.NODE_TEMPLATE[nodeIdx]
    return tpl and tpl[2] or "normal"
end

--- 获取节点显示名
---@param nodeIdx number 1~24
---@return string
function M.GetNodeDisplayName(nodeIdx)
    local tpl = M.NODE_TEMPLATE[nodeIdx]
    return tpl and tpl[3] or ("节点" .. nodeIdx)
end

--- 计算单图总体力消耗
---@return number
function M.GetMapTotalStamina()
    local total = 0
    for i = 1, #M.NODE_TEMPLATE do
        total = total + M.GetNodeStamina(i)
    end
    return total
end

--- 获取某章节所有地图列表
---@param chapter number 1~10
---@return MapConfig[]
function M.GetChapterMaps(chapter)
    local startId = (chapter - 1) * 10 + 1
    local endId = chapter * 10
    local list = {}
    for id = startId, endId do
        if M.MAPS[id] then
            list[#list + 1] = M.MAPS[id]
        end
    end
    return list
end

----------------------------------------------------------------------------
-- 解锁判定 API
----------------------------------------------------------------------------

--- Boss 节点的 nodeId (NODE_TEMPLATE 第24项)
M.BOSS_NODE_ID = 24

--- 检查某图是否已通关 (boss节点已击败)
---@param mapId number
---@param state table 玩家状态 (需含 nodeStars)
---@return boolean
function M.IsMapCleared(mapId, state)
    if not state or not state.nodeStars then return false end
    local bossKey = mapId .. "_" .. M.BOSS_NODE_ID
    return (state.nodeStars[bossKey] or 0) > 0
end

--- 检查某图是否解锁
--- 规则: 第1图默认解锁; 后续图需前一图boss已击败
---@param mapId number
---@param state table
---@return boolean
function M.IsMapUnlocked(mapId, state)
    if mapId <= 1 then return true end
    return M.IsMapCleared(mapId - 1, state)
end

--- 检查某章节是否解锁
--- 规则: 第1章默认解锁; 后续章需前一章所有10图的boss全部击败
---@param chapter number 1~10
---@param state table
---@return boolean
function M.IsChapterUnlocked(chapter, state)
    if chapter <= 1 then return true end
    -- 前一章的10图 boss 都需要通关
    local prevStart = (chapter - 2) * 10 + 1
    local prevEnd   = (chapter - 1) * 10
    for id = prevStart, prevEnd do
        if not M.IsMapCleared(id, state) then
            return false
        end
    end
    return true
end

--- 获取玩家当前可进入的最高图
---@param state table
---@return number
function M.GetMaxUnlockedMap(state)
    for id = 100, 1, -1 do
        if M.IsMapUnlocked(id, state) then
            return id
        end
    end
    return 1
end

return M
