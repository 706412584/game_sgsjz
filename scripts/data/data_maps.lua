local POWER_ANCHORS = {
    [1]   = 1800,
    [10]  = 9300,
    [20]  = 38000,
    [30]  = 140000,
    [40]  = 480000,
    [50]  = 1500000,
    [60]  = 4000000,
    [70]  = 10000000,
    [80]  = 25000000,
    [90]  = 60000000,
    [100] = 150000000,
}

-- 已知锚点排序
local anchorKeys = {}
for k in pairs(POWER_ANCHORS) do anchorKeys[#anchorKeys + 1] = k end
table.sort(anchorKeys)

--- 通过指数插值计算任意图号的推荐战力
---@param mapId number
---@return number
local function calcPower(mapId)
    if POWER_ANCHORS[mapId] then return POWER_ANCHORS[mapId] end
    -- 找到左右锚点
    local lo, hi = anchorKeys[1], anchorKeys[#anchorKeys]
    for i = 1, #anchorKeys - 1 do
        if anchorKeys[i] <= mapId and anchorKeys[i + 1] >= mapId then
            lo = anchorKeys[i]
            hi = anchorKeys[i + 1]
            break
        end
    end
    local pLo = POWER_ANCHORS[lo]
    local pHi = POWER_ANCHORS[hi]
    local t = (mapId - lo) / (hi - lo)
    -- 指数插值: p = pLo * (pHi/pLo)^t
    return math.floor(pLo * (pHi / pLo) ^ t)
end

