-- ============================================================================
-- 《问道长生》属性体系数据配置
-- 数据来源: docs/game-design-values.md §2
-- ============================================================================

local M = {}

-- ============================================================================
-- 2.1 基础属性初始值
-- ============================================================================
M.INITIAL_STATS = {
    hp      = 800,   -- 气血
    hpMax   = 800,
    mp      = 200,   -- 灵力
    mpMax   = 200,
    attack  = 45,    -- 攻击
    defense = 30,    -- 防御
    speed   = 20,    -- 速度
    crit    = 5,     -- 暴击(%)
    dodge   = 3,     -- 闪避(%)
    hit     = 75,    -- 命中(%)
}

-- ============================================================================
-- 2.2 修真属性初始值
-- ============================================================================
M.INITIAL_CULTIVATION = {
    rootBone   = "中品灵根",
    wisdom     = 72,       -- 悟性
    fortune    = "普通",    -- 气运
    daoHeart   = 0,         -- 道心（数字，0=未磨砺；悟道/突破自动增长）
    sense      = 0,        -- 神识
}

-- ============================================================================
-- 2.3 灵根品质
-- ============================================================================
-- rate: 修炼速度倍率
-- prob: 创角随机概率(%)
-- ============================================================================
M.SPIRIT_ROOTS = {
    { name = "废灵根",  rate = 0.5, prob = 10 },
    { name = "下品灵根", rate = 0.8, prob = 30 },
    { name = "中品灵根", rate = 1.0, prob = 35 },
    { name = "上品灵根", rate = 1.5, prob = 20 },
    { name = "天灵根",  rate = 2.0, prob = 5 },
}

-- ============================================================================
-- 2.4 气运等级
-- ============================================================================
-- drop: 掉落加成(%)
-- alchemy: 炼丹加成(%)
-- ============================================================================
M.FORTUNE_LEVELS = {
    { name = "低迷", drop = -10, alchemy = -5,  source = "渡劫失败/随机事件" },
    { name = "普通", drop = 0,   alchemy = 0,   source = "初始" },
    { name = "小吉", drop = 5,   alchemy = 5,   source = "鸿运丹/随机事件" },
    { name = "大吉", drop = 10,  alchemy = 10,  source = "鸿运丹x3/稀有事件" },
    { name = "天命", drop = 20,  alchemy = 15,  source = "极其稀有" },
}

-- ============================================================================
-- 2.5 道心等级
-- ============================================================================
M.DAO_HEARTS = { "不稳", "稳固", "坚定", "不动" }

-- ============================================================================
-- 辅助函数
-- ============================================================================

--- 根据灵根名获取倍率
---@param name string
---@return number
function M.GetRootRate(name)
    for _, r in ipairs(M.SPIRIT_ROOTS) do
        if r.name == name then return r.rate end
    end
    return 1.0
end

--- 根据气运名获取配置
---@param name string
---@return table|nil
function M.GetFortune(name)
    for _, f in ipairs(M.FORTUNE_LEVELS) do
        if f.name == name then return f end
    end
    return nil
end

--- 随机生成灵根(按概率加权)
---@return string
function M.RandomRoot()
    local roll = math.random(100)
    local acc = 0
    for _, r in ipairs(M.SPIRIT_ROOTS) do
        acc = acc + r.prob
        if roll <= acc then return r.name end
    end
    return "中品灵根"
end

return M
